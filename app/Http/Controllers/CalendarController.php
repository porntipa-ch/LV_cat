    public function getTrips()
    {
        $trips = Trip::all();

        $data = array();

        foreach ($trips as $trip) {
            $data[] = array(
                'title' => $trip->trip_name, // The name of the trip
                'start' => $trip->start_date,
                'end' => $trip->end_date,
            );
        }

        return response()->json($data);
    }
